package QuickSort;

public class Basic {
    /*
     Quick Sort is a divide and conquer algorithm. It involves 3 steps -

     Pivot Section - We pick an element and mark it as pivot . The pivot element can be first
     element ,last element or any random element.

     Partitioning - We reorder the array such that all elements
     greater than pivot comes after the pivot and all elements smaller than
     pivot comes before the pivot . The elements equal to pivot can go either side
     of the pivot.After this partitioning the pivot is at its correct sorted position.

     Recursion - Recursively apply the above steps on the sub array formed to the left side of pivot
     and on the sub array formed on the right side of the pivot .

     */
}

