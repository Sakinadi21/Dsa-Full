public class Basic {
    /*
    Merge Sort is a divide and conquer algorithm.
                Divide and Conquer recursively breaks down a problem into two or more sub-problems
                of the same or related type , until these become simple enough to be solved directly.
                The solutions to the sub-problems are then combined to give a solution to the
                original problem.

     Divide - In this step the algorithm takes middle point of array
      and divides the array into 2 halves . The algorithm is carried out
      recursively for half arrays, until there are no more half arrays to divide .

      Conquer - In this step starting from the bottom,
      we sort and merge the divided arrays and get the sorted arrays

     */
}
