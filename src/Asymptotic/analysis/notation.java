   /*
    Asymptotic Notations are the mathematical tools used to describe

    the running time of an algorithm in terms of input size.

        Example - Performance of car in 1 litre of petrol


    Highway(min traffic)-25 km/litre
    City(max traffic)-15 km/litre
    City + Highway (avg traffic) -20km /litre


    Asymptotic Notations help us in determining
            1.Best case
            2.Average case
            3.Worst case

    There are three notations for performing runtime analysis of an algorithm -
    1.omega notation
            It is the formal way to express the lower bound of an algorithm's running time.
            Lower bound means for any given input this notation determines best amount of
            time  an algorithm can take to  complete.

     For example -
     if we say certain algorithm takes 100 secs as best amount of time . So, 100 secs will be lower
     bound of that algorithm . The algorithm can take more than 100 secs but it will not take less
     than 100 secs.


     2.BigO(0) Notation
     It is the formal way to express the upper bound of an algorithm's running time.
     Upper bound means for any given input this notation determines longest amount
     of time an algorithm can take to complete.

     For example-
     If we say certain algorithm takes 100 secs as longest amount of time.So,100 secs
     will be upper bound of that algorithm. The algorithm can take less than 100 secs,
     but it will not take more than 100 secs but it will not take more than 100 secs.


     3.Theta(0/) Notation
     It is the formal way to express both the upper and lower bound of an algorithm's
     running time.
     By lower and Upper bound means for any given input this notation
     determines average amount of time an algorithm can take to complete.

      For example-
      If we run certain algorithm, and it takes 100 secs for first run , 120 secs for
      second run, 110 for third run and so on. So, theta notations gives an average
      of running time of that algorithm.
 */

